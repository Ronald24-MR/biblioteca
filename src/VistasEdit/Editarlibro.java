    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int fila = tabla.getSelectedRow();
        if(fila == -1){
            JOptionPane.showMessageDialog(this, "Debe seleccionar una fila");
        }else{
            if(fila >= 0){
                int codigo=Integer.parseInt(tabla.getValueAt(fila, 0).toString());

                Libros libros=dt.findLibros(codigo);
                
                c1.setText(libros.getCodigo().toString());
                c1.setEditable(false);
                c2.setText(libros.getTitulo());
                c3.setText(libros.getEditorial());
                c4.setText(""+libros.getVigencia());
                c5.setText(""+libros.getPrecio());
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

