    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int fila = tabla.getSelectedRow();
        if(fila == -1){
            JOptionPane.showMessageDialog(this, "Debe seleccionar una fila y luego importarla");
        }
        else
        {
            int codigo=Integer.parseInt(c1.getText());
            String titulo=c2.getText();
            String editorial=c3.getText();
            int vigencia=Integer.parseInt(c4.getText());
            int precio=Integer.parseInt(c5.getText());

            Libros datosl=dt.findLibros(codigo);
            datosl.setCodigo(codigo);
            datosl.setTitulo(titulo);
            datosl.setEditorial(editorial);
            datosl.setVigencia(vigencia);
            datosl.setPrecio(precio);

            try {
                dt.edit(datosl);
                JOptionPane.showMessageDialog(null,"Se Actualizaron los registros");
                Cargartabla();
            } catch (NonexistentEntityException ex) {

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null,"Se presento un error al guardar los registros");
            }

        }
    }//GEN-LAST:event_jButton2ActionPerformed

