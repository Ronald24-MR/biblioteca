    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int fila = tabla.getSelectedRow();
        if(fila == -1){
            JOptionPane.showMessageDialog(this, "Debe seleccionar una fila");
        }else{
            if(fila >= 0){
                int codigo=Integer.parseInt(tabla.getValueAt(fila, 0).toString());

                RegistroRegreso rp = r.findRegistroRegreso(codigo);
                
                c2.setText(rp.getCodigo().toString());
                c2.setEditable(false);
                c1.setSelectedItem(rp.getRegistroprestamoCodigo());
                //fecha
                c4.setText(rp.getObservaciones());
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

