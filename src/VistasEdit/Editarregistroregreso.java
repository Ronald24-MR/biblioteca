    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int fila = tabla.getSelectedRow();
        if(fila == -1){
            JOptionPane.showMessageDialog(this, "Debe seleccionar una fila y luego importarla");
        }
        else
        {
            int codigo=Integer.parseInt(c2.getText());
            //date1
            //Date fecha1=c3.getDate();
            //DateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
            //String fecha = dateFormat.format(fecha1);
            //
            String observaciones=c4.getText();
            RegistroPrestamo registroprestamo=(RegistroPrestamo) c1.getSelectedItem();
            RegistroRegreso registroregreso=r.findRegistroRegreso(codigo);
            registroregreso.setCodigo(codigo);
            //registroregreso.setFecharegreso(fecha1);
            registroregreso.setObservaciones(observaciones);
            registroregreso.setRegistroprestamoCodigo(registroprestamo);

            if (registroregreso==null) {
                try {
                    r.edit(registroregreso);
                    JOptionPane.showMessageDialog(null,"Se actualizaron todos los registros");
                    Cargartabla();
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null,"Se presento un error");
                }
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Se presento un error");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

