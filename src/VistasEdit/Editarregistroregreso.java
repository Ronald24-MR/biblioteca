    public void Cargartabla(){
        DefaultTableModel modelo1=new  DefaultTableModel();
        String nombrecolumnas[]={"Codigo","Fecha regreso","Observaciones","Registro prestamo"};
        modelo1.setColumnIdentifiers(nombrecolumnas);
        List lista=r.findRegistroRegresoEntities();
        DateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        for (int i = 0; i < lista.size(); i++) {
            RegistroRegreso r=(RegistroRegreso) lista.get(i);
            Object fila[]={r.getCodigo(),dateFormat.format(r.getFecharegreso()),r.getObservaciones(),r.getRegistroprestamoCodigo().getCodigo()};
            modelo1.addRow(fila);
        }
        tabla.setModel(modelo1);
    }
