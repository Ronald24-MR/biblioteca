    public void Cargartabla(){
    DefaultTableModel modelo1=new  DefaultTableModel();
    String nombrecolumnas[]={"Codigo","Tarjeta de credito","Cvv","Fecha prestamo","Fecha entrega","Vigencia","Libros","Mes","Tipo de pago","Cedula"};
    modelo1.setColumnIdentifiers(nombrecolumnas);
    List lista=rg.findRegistroPrestamoEntities();
    DateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
    DateFormat dateFormat2 = new SimpleDateFormat("dd/MM/yyyy");
    

        for (int i = 0; i < lista.size(); i++) {
            RegistroPrestamo r=(RegistroPrestamo) lista.get(i);
            Object fila[]={r.getCodigo(),r.getTarjetacredito(),r.getCvv(),dateFormat.format(r.getFechaprestamo()),dateFormat2.format(r.getFechaentrega()),r.getVigenciaCodigo().getNombre(),r.getLibrosCodigo().getTitulo(),r.getMesCodigo().getNombre(),r.getTipopagoCodigo().getNombre(),r.getRegistroCedula().getCedula()};
            modelo1.addRow(fila);
        }
    tabla.setModel(modelo1);
    }
