    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int fila = tabla.getSelectedRow();
        if(fila == -1){
            JOptionPane.showMessageDialog(this, "Debe seleccionar una fila y luego importarla");
        }
        else
        {
            int codigo=Integer.parseInt(txtCodigo.getText());
            int tarjeta=Integer.parseInt(txtTarjeta.getText());
            int cvv = Integer.parseInt(txtCvv.getText());
            //fecha prestamo
            //fecha entrega
            Vigencia vigencia = (Vigencia)txtVigencia.getSelectedItem();
            Libros libros = (Libros)txtLibros.getSelectedItem();
            Mes mes = (Mes)txtMes.getSelectedItem();
            TipoPago pago = (TipoPago)txtPago.getSelectedItem();
            Registro registro = (Registro)txtRegistro.getSelectedItem();
            

            RegistroPrestamo r = rg.findRegistroPrestamo(codigo);
            
            r.setCodigo(codigo);
            r.setTarjetacredito(tarjeta);
            r.setCvv(cvv);
            r.setVigenciaCodigo(vigencia);
            r.setLibrosCodigo(libros);
            r.setMesCodigo(mes);
            r.setTipopagoCodigo(pago);
            r.setRegistroCedula(registro);
            
            try {
                rg.edit(r);
                JOptionPane.showMessageDialog(this, "Se actualizaron los registros");
                Cargartabla();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Error al actualizar");
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        int fila = tabla.getSelectedRow();
        if(fila == -1){
            JOptionPane.showMessageDialog(this, "Debe seleccionar una fila");
        }else{
            if(fila >= 0){
                int codigo=Integer.parseInt(tabla.getValueAt(fila, 0).toString());

                RegistroPrestamo r = rg.findRegistroPrestamo(codigo);
                
                txtCodigo.setText(r.getCodigo().toString());
                txtCodigo.setEditable(false);
                String tarjeta = String.valueOf(r.getTarjetacredito());
                txtTarjeta.setText(tarjeta);
                String cvv = String.valueOf(r.getCvv());
                txtCvv.setText(cvv);
                //fecha presstamo
                //fecha entrega
                txtVigencia.setSelectedItem(r.getVigenciaCodigo());
                txtLibros.setSelectedItem(r.getLibrosCodigo());
                txtMes.setSelectedItem(r.getMesCodigo());
                txtPago.setSelectedItem(r.getTipopagoCodigo());
                txtRegistro.setSelectedItem(r.getRegistroCedula());
                
                
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

